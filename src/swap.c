#include "push_swap.h"

void	swap(int *ar)
{
	int temp;

	temp = ar[0];
	ar[0] = ar[1];
	ar[1] = temp;;
}

void	swap_ab(int *a, int *b)
{
	swap(a);
	swap(b);
}
