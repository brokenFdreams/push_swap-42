#include "libft.h"

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	unsigned char	ch;
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	ch = (unsigned char)c;
	d = (unsigned char*)dst;
	s = (unsigned char*)src;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		if (s[i] == ch)
			return (dst + i + 1);
		i++;
	}
	return (NULL);
}
