#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int				i;
	unsigned int	n;

	i = 0;
	n = 0;
	if (s && f)
	{
		while (s[i])
		{
			f(n, &s[i]);
			i++;
			n++;
		}
	}
}
